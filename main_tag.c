/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	unsigned short adc1=0;
	unsigned short adc2=0;
	unsigned short adc3=0;
	unsigned short adc4=0;
	unsigned short adc5=0;
	unsigned short adc6=0;
	unsigned short sum=0;
	unsigned short count_a=0;


  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_SPI1_Init();
  MX_ADC1_Init();
  MX_USART1_UART_Init();
  MX_TIM2_Init();
  MX_I2C1_Init();
  /* USER CODE BEGIN 2 */
  HAL_Delay(1000);
  DF_Init(20);
  DF_Play(0x06);

  //ss_tag_main();
  //HAL_TIM_Base_Start_IT(&htim2);
     //ds_resp_main();
   /******************/

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  if (HAL_ADCEx_Calibration_Start (&hadc1) != HAL_OK)
  {
	  Error_Handler();
  }

  if(HAL_ADC_Start_DMA (&hadc1, (uint32_t*) &uwADCxConvertedValue,3) != HAL_OK)
  {
	  Error_Handler ();
  }


  ss_resp_main();
  dwt_setinterrupt(SYS_STATUS_RXFCG, 1);
  dwt_rxenable(DWT_START_RX_IMMEDIATE);

  while (1)
  {
    /* USER CODE END WHILE */
	adc1 = uwADCxConvertedValue[0];
	adc2 = uwADCxConvertedValue[1];
	adc3 = uwADCxConvertedValue[2];
	sum = 0.9 * abs(adc1 - adc4) + 0.5 * abs(adc2 - adc5) + 0.8 * abs(adc3 - adc6);

	if (sum > 500) {
		count_a++;
		if (count_a > 3)
		{
			shock_flag = 1;
			shock_test = 1;
			DF_Play(0x01); // 10s after send sound
			shock_time = HAL_GetTick();
		}
	} else {
		count_a = 0;
	}

	current_time = HAL_GetTick();

	if ((sos_start_flag == 1 && sos_test == 1) || (shock_flag == 1 && shock_test == 1)) {

		if (sos_start_flag == 1 && sos_test == 1) {
			if(current_time-second_sos_time>10000) {
				DF_Play(0x02); //server send sound
				sos_flag = 1;
				sos_test = 0;
			}
		}

		if (shock_flag == 1 && shock_test == 1){
			if ((current_time - shock_time) >= 10000) {
				DF_Play(0x02); //server send sound
				server_shock_flag = 1;
				shock_test = 0 ;
			}
		}
	}

	adc4 = adc1;
	adc5 = adc2;
	adc6 = adc3;

	usleep(10000);
    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

